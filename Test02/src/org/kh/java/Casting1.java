package org.kh.java;

public class Casting1 {

	public static void main(String[] args) {
		byte b = 20;//1byte
		short s = b; //2byte
		int i = s; //4byte
		long l = i; //8byte
		//작은 크기의 데이터값 -> 큰 크기의 데이터 값 
		//이 되버리면서 타입 자동으로 변경, 따라서 역순은 강제 변경해야함
		//Force Casting: 데이터 값을 강제 변경해야함
		int i2 = (int) l;
		short s2 = (short) i2;
		byte b2 = (byte) s2;
		
	}

}
